<p id="en5">
  [<a href="#ref5">5</a>]: There are four recognized gender groups in Huetopian society: Long hair,
  No facial hair; Long hair Facial Hair; Short Hair no facial hair; and Short hair, facial hair.
  TriVerment laws require 3 consenting adults (at least 27 years of age) of different groupings to
  solidify a union. Physiologically, 3 adults are needed for a child. The stereotypical middle class
  family has 3 parents and 8 kids. In reality, the average number of children per family is
  typically closer to 3, but TriVerment propaganda wants to see a larger population of people loyal
  to the TriVerment, so they use propaganda to make it patriotic to have 9 children.
</p>
