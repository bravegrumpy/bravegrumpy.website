<h5>Attitudes about Euclidean School<sup id="ref4"><a href="#en4">4</a></sup></h5>
<p>
  Her peers thought she was super fun to be around and aspired to be like her. She schooled even the
  most athletic of her peers at sports and could be the life of the party. However, this extreme
  charisma never reached her eyes. She always thought she didn't fit in. However, part of it was the
  weight of keeping her community's secrets while at school. Acting like the perfectly trained
  Angular seemed fun but was exhausting.
</p>
<p>
  It is tough to hide her Ellipsoid roots when accidentally cursing. Great grandparents came from a
  time when it wasn't as essential to hide, so her grandparents and parents often used ellipsoid
  turns of phrases around the house.
</p>
<h5>If found in a coffee shop, what would they be doing? What would they be drinking?</h5>
<p>
  As a child, K-ler rarely went to coffee shops, as she would have rather been found climbing trees
  in the woods.
</p>
<p>
  When becoming an adult, and finding that she was meant to be a historian, she would go to coffee
  shops, and get the sweetest hot tea they had, usually orange, or lemon. She then would be seen
  with a stack of 3-5 well-worn books, a calligraphy pen, a pot of ink, and some paper. Even though
  most people would write with a [BrightLight], K-ler's tendency to be a color magic adept causes
  her to have this peculiar love for the pen and paper. Canonically, she is approximately 6
  generations younger than the cast of characters we will follow on this adventure, so much of the
  narrative is pieced together. (Apparently I as the “irl” Skylar am translation from Sinish)
</p>
<h5>When Flirting</h5>
<p>
  Skylar is not much of a flirt, and cannot tell if anybody is flirting with her. She also has some
  serious problems with generic Triagonal society, with the typical 3-parent, 9-kid generic life.
  She somehow ended up with only one life partner (Wildly unusual in her society), and her life
  partner is another femme (long hair no facial hair<sup id="ref5"><a href="#en5">5</a></sup>). That
  being said, she does dutifully record what primary sources tell her, so she will record
  relationships properly.
</p>
<h5>What does her room look like</h5>
<p>
  Even though she comes off as disheveled when dressing, she actually keeps her living space
  pristine. Her bedroom has a single bed tucked in the corner farthest from the door, on a metal bed
  frame, and no headboard. Her and her partner insist on keeping separate sleeping spaces, as to not
  annoy each other. At the head of her bead, at height about 2 feet above where her head rests, when
  sitting up in bed, there is a shelf. On the shelf, there is a collection of blue and grey painting
  supplies. Above the shelf, there is a rectangular patch that is constantly being painted. Right
  now, there is half of an oceanic scene, covering some partial runes that are part of a sleeping
  spell. Known only to her, and Ellipsoid people, this oceanic scene is also a more powerful
  sleeping spell.
</p>
<p>
  In the other corner, adjacent to the head of the bed, there is a wire rack, with a set of hangars,
  with clothing neatly hung up. Beside the rack, there is a wooden drawer set. Between the drawers
  and the bed, there is a window, with bright yellow shears, to allow plenty of light through.
</p>
<p>
  Across from the window, lies a writing desk with a tablet, a cup full of pens, and a pad of paper.
  In the righthand drawer of the writing desk, she keeps a set of empty pads, and below that lives a
  filing cabinet. To the right of the desk, lives an overflowing bookcase. Each shelf is stuffed
  completely, and stacked above the bookcase, there are three piles of neatly stacked books.
</p>
<p>
  Wrapping around the whole room, skipping the painting, there are floating shelves, also all filled
  with books, however these books are held up using some metal bookends. At the bottom of each shelf
  (including the bookcase) there are neatly handwritten labels, describing the genre of book that
  lives here. If closely inspected, each of the books are organized by the author's middle name in
  each section. Upon closer inspection, one would notice that at the top of each book, just above
  the binding, there are some markers, there for possibly some organization purpose, unknown to
  anybody other that K-ler.
</p>
<h5>Story Role</h5>
<p>
  K-Ler is a narrative foil to Professor Arch. She will also foreshadow the big reveal that the
  TriVerment is a dystopian government rather than a generic, ignorable entity. She is the one
  student who knows that Sikland is not homogeneous and that the cultures of Sikland are not
  entirely dead. It is also essential that K–Ler is never a POV character, unless K-Ler is the
  author. However, if K-Ler is the narrator, then she would not actually exist within the narrative
  at all, but will show signs of being a biased narrator.
</p>
