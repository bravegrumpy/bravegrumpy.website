<h5>Special Skills</h5>
<p>
  Character X knows the prayers, summoning the Dragon of the River, and many other spirits. She is
  skeptical, given that she has not seen any firsthand. She does not know that the visions come at a
  Color Magic Adept ceremony immediately after her Ph.D. graduation.
</p>
<h5>Occupation</h5>
<p>
  X is a student at The Academy. While her parents could pay the tuition, she also got a partial
  scholarship because of her unnaturally high color-magic powers.
</p>
<p>
  X eventually graduates, and becomes a historian, to right the incorrect record that Arch provided.
  K-ler is the author of this tale, and therefore almost everything will be colored through K-ler's
  her lens. The reader won't know that she is a her. Or much of what this is about.
</p>
<p>
  Her role as a narrator might come through when we read a book by Professor Arch, in the next
  volume.
