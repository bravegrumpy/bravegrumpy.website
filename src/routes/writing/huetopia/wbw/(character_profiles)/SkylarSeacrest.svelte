<h5>Region-Of-Origin</h5>
<p>
  One of the ellipsoid tribes on the outskirts of the Great Circular Lake. This territory was taken
  over by the Triangle nation about one thousand years ago, so many of her nation's cultural
  practices have been influenced by Triangle culture. Most people have never heard of the ellipsoid
  tribes. Their lakes, the Ellipses, have since been renamed by the Triangles as the Angular
  Rivlettes.
</p>
