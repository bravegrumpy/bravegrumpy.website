<h5>Roots</h5>
<p>
  Grew up in the inner projects of the old T-Fort City. He was secretly a color-magic adept. Once he
  got through Euclidian school (the equivalent to K-12 in Huetopia), the TriVerment offered him a
  job scouting for other color-magic adepts<sup id="ref1"><a href="#en1">1</a></sup> in addition to doing
  product development.
</p>
<h5>Patents</h5>
<p>
  The modern BrightLight<sup id="ref2"><a href="#en2">2</a></sup> technology. This is the lighting technology
  that allows for modern-day food production.
</p>
<p>That being said, Atti stole the idea from some Scholars in the Ancient Artist Temple.</p>
<p>
  The widespread use of printing and stickers was previously the best method of using the
