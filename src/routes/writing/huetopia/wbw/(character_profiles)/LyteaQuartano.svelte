<h4>Lytia Quartano</h4>
<h5>Region of Origin</h5>
<p>
  Lytia is a Linarian, from one of the Old Families<sup id="ref14"><a href="#en14">14</a></sup>.
</p>
<h5>Personality</h5>
<p>
  Lytea grew up surrounded by total luxury, but was the 15th child in a family with 16 kids. While
  the oldest few sent to the most prestigious Euclidean schools, and were doted on by all four
  parents, as more kids came, more parenting responsibilities fell to the housekeeping staff.
</p>
